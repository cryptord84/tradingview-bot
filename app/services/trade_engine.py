    def _resolve_token_mint(self, symbol: str) -> str:
        """Resolve token symbol to Solana mint address."""
        # Check well-known mints first, then rebalancer config
        if symbol in self._KNOWN_MINTS:
            return self._KNOWN_MINTS[symbol]
        rebal_cfg = get("rebalancer") or {}
        token_mints = rebal_cfg.get("token_mints", {})
        if symbol in token_mints:
            return token_mints[symbol]
        # Fallback to SOL mint if unknown
        logger.warning(f"Unknown token mint for {symbol}, falling back to SOL")
        return self._KNOWN_MINTS["SOL"]

    def _token_decimals(self, symbol: str) -> int:
        """Get decimal places for a token (for lamport conversion)."""
        return self._TOKEN_DECIMALS.get(symbol, 9)

    @staticmethod
    def _get_strategy_tp_sl(
        strategy: Optional[str],
        symbol: Optional[str] = None,
        timeframe: Optional[str] = None,
    ) -> tuple[float, float]:
        """Get TP/SL multipliers for a strategy/token/TF, falling back to global defaults.

        Precedence (highest first):
          1. strategy_token_tf_overrides[strategy][token][tf] — from tp_sl_sweep winners
          2. position_monitor.token_overrides[token].sl_multiplier — manual SL widening
          3. strategy_profiles[strategy] — hand-tuned per-strategy defaults
          4. position_monitor.{tp_multiplier, sl_multiplier} — global defaults
        """
        pos_cfg = get("position_monitor") or {}
        default_tp = pos_cfg.get("tp_multiplier", 4.0)
        default_sl = pos_cfg.get("sl_multiplier", 1.5)

        tp_mult, sl_mult = default_tp, default_sl

        # Strategy-specific overrides (hand-tuned)
        if strategy:
            strategy_cfg = get("strategy_profiles") or {}
            strategy_upper = strategy.upper()
            for name, profile in strategy_cfg.items():
                if strategy_upper.startswith(name.upper()):
                    tp_mult = profile.get("tp_multiplier", default_tp)
                    sl_mult = profile.get("sl_multiplier", default_sl)
                    break

        # Token-specific SL override (e.g., memecoins need wider stops)
        token_key = None
        if symbol:
            token_key = symbol.upper().replace("USDT", "").replace("USD", "")
            token_overrides = pos_cfg.get("token_overrides") or {}
            override = token_overrides.get(token_key)
            if override and "sl_multiplier" in override:
                sl_mult = override["sl_multiplier"]

        # Sweep-generated per-(strategy, token, tf) overrides — highest precedence.
        # Pine sends "EMA Ribbon v1.0" / "240"; YAML is keyed by "EMA Ribbon" / "4H".
        if strategy and token_key and timeframe:
            sweep = _load_tpsl_overrides()
            strat_norm = _normalize_strategy_name(strategy)
            tf_norm = _normalize_timeframe(timeframe)
            slot = sweep.get(strat_norm, {}).get(token_key, {}).get(tf_norm)
            if slot:
                tp_mult = slot.get("atr_tp_mult", tp_mult)
                sl_mult = slot.get("atr_sl_mult", sl_mult)

        return tp_mult, sl_mult

