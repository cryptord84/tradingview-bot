# Map Pine `_strat` string (e.g. "EMA Ribbon v1.0") → backtest canonical name.
# Backtest uses short names; Pine sends full "<Name> v<ver>". Keep explicit — easier to debug than regex.
_STRATEGY_NAME_ALIASES = {
    "ema ribbon": "EMA Ribbon",
    "stoch rsi": "Stoch RSI",
    "fvg": "FVG",
    "vwap dev": "VWAP Dev",
    "vwap deviation": "VWAP Dev",
    "liq sweep": "Liq Sweep",
    "liquidity sweep": "Liq Sweep",
    "donchian": "Donchian",
    "donchian breakout": "Donchian",
}


def _normalize_strategy_name(raw: Optional[str]) -> Optional[str]:
    """'EMA Ribbon v1.0' → 'EMA Ribbon'. Returns None if no match."""
    if not raw:
        return None
    # Strip trailing " v<digits>..." version tag
    base = raw.strip()
    for tag_sep in (" v", " V"):
        idx = base.rfind(tag_sep)
        if idx > 0 and idx < len(base) - 1 and base[idx + 2].isdigit():
            base = base[:idx]
            break
    return _STRATEGY_NAME_ALIASES.get(base.lower(), base)


# Pine `timeframe.period` emits raw resolution codes. Backtest uses human labels.
_TIMEFRAME_ALIASES = {
    "15": "15m", "15m": "15m",
    "60": "1H",  "1h": "1H", "1H": "1H",
    "240": "4H", "4h": "4H", "4H": "4H",
    "D": "1D",   "1D": "1D", "1d": "1D",
}


def _normalize_timeframe(raw: Optional[str]) -> Optional[str]:
    """'240' → '4H', '60' → '1H'. Returns input unchanged if not mapped."""
    if not raw:
        return None
    return _TIMEFRAME_ALIASES.get(raw.strip(), raw)


def get_sizing_override(strategy: Optional[str], symbol: Optional[str],
                        timeframe: Optional[str]) -> Optional[dict]:
    """Look up tier sizing for this (strategy, token, tf). Returns {tier, size_pct, pf} or None."""
    strat = _normalize_strategy_name(strategy)
    tf = _normalize_timeframe(timeframe)
    if not (strat and symbol and tf):
        return None
    token = symbol.upper().replace("USDT", "").replace("USD", "")
    slot = _load_sizing_overrides().get(strat, {}).get(token, {}).get(tf)
    if not slot:
        return None
    return slot


def _strip_pair(symbol: str) -> str:
    """Normalise 'BONKUSDT' → 'BONK' for correlation lookups."""
    return symbol.upper().replace("USDT", "").replace("USD", "")


def correlation_check(symbol: str) -> Optional[dict]:
    """Check if opening a new long in `symbol` would exceed its correlation group cap.

    Returns None if allowed, or a dict {group, limit, held: [tokens]} if blocked.
    """
    cfg = get("correlation") or {}
    if not cfg.get("enabled"):
        return None
    groups = cfg.get("groups") or {}

    tok = _strip_pair(symbol)
    # Find which group this token belongs to (first match wins)
    group_name = None
    group_cfg = None
    for gname, gcfg in groups.items():
        if tok in {t.upper() for t in (gcfg.get("tokens") or [])}:
            group_name = gname
            group_cfg = gcfg
            break
    if group_name is None:
        return None  # untracked token, no cap

    limit = int(group_cfg.get("max_concurrent", 99))
    members = {t.upper() for t in (group_cfg.get("tokens") or [])}
    held = [
        _strip_pair(p.get("symbol", ""))
        for p in get_open_positions()
        if _strip_pair(p.get("symbol", "")) in members
    ]
    # Don't double-count: if an open position already exists on the same symbol,
    # this BUY is likely a pyramid/add — let Claude + risk manager handle that,
    # correlation cap only applies to *new* distinct symbols.
    distinct_held = set(held) - {tok}
    if len(distinct_held) >= limit:
        return {
            "group": group_name,
            "limit": limit,
            "held": sorted(distinct_held),
            "token": tok,
        }
    return None


